public class Calc {
    public int n1;
    public int n2;

    public int somar() {
        return n1 + n2;
    }

    public int diminuir() {
        return n1 - n2;
    }

    public int multiplicar() {
        return n1 * n2;
    }

    public double dividir() {
        return (double) n1 / n2;
    }
}